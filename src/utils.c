long	ft_atol(const char *str)
{
	long	res;
	int		sign;
	int		i;

	res = 0;
	sign = 1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return (res * sign);
}

int	stack_len(t_stack *stack)
{
	int	count;

	if (!stack)
		return (0);
	count = 0;
	while (stack)
	{
		stack = stack->next;
		count++;
	}
	return (count);
}

void	assign_index(t_stack *a)
{
	t_stack	*tmp;
	t_stack	*tmp2;
	int		ind;

	tmp = a;
	while (tmp)
	{
		ind = 0;
		tmp2 = a;
		while (tmp2)
		{
			if (tmp2->value < tmp->value)
				ind++;
			tmp2 = tmp2->next;
		}
		tmp->index = ind;
		tmp = tmp->next;
	}
}

int	is_sorted(t_stack *a)
{
	while (a && a->next)
	{
		if (a->value > a->next->value)
			return (0);
		a = a->next;
	}
	return (1);
}

